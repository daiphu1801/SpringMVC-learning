-- Drop sequences if they exist
DROP SEQUENCE IF EXISTS APP_USER_SEQ;
DROP SEQUENCE IF EXISTS APP_CATEGORY_SEQ;
DROP SEQUENCE IF EXISTS APP_PRODUCT_SEQ;
DROP SEQUENCE IF EXISTS APP_ORDER_SEQ;
DROP SEQUENCE IF EXISTS APP_ORDER_ITEM_SEQ;

-- Drop tables if they exist
DROP TABLE IF EXISTS APP_ORDER_ITEMS;
DROP TABLE IF EXISTS APP_ORDERS;
DROP TABLE IF EXISTS APP_USER_ADDRESSES;
DROP TABLE IF EXISTS APP_PRODUCTS;
DROP TABLE IF EXISTS APP_CATEGORIES;
DROP TABLE IF EXISTS APP_USERS;

-- Create sequences
CREATE SEQUENCE APP_USER_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE APP_CATEGORY_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE APP_PRODUCT_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE APP_ORDER_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE APP_ORDER_ITEM_SEQ START WITH 1 INCREMENT BY 1;

-- Create tables
CREATE TABLE APP_USERS (
    ID NUMBER PRIMARY KEY,
    USERNAME VARCHAR2(100) UNIQUE,
    FULL_NAME VARCHAR2(200),
    EMAIL VARCHAR2(200),
    PHONE VARCHAR2(50),
    STATUS VARCHAR2(50),
    PASSWORD VARCHAR2(200),
    ROLE VARCHAR2(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE APP_CATEGORIES (
    ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(200),
    CODE VARCHAR2(100),
    DESCRIPTION VARCHAR2(1000),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE APP_PRODUCTS (
    ID NUMBER PRIMARY KEY,
    CATEGORY_ID NUMBER,
    SKU VARCHAR2(100) UNIQUE,
    NAME VARCHAR2(200),
    DESCRIPTION VARCHAR2(2000),
    PRICE NUMBER,
    STATUS VARCHAR2(50),
    IMAGE_URL VARCHAR2(500),
    STOCK NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_PRODUCT_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES APP_CATEGORIES(ID)
);

CREATE TABLE APP_USER_ADDRESSES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER,
    RECEIVER_NAME VARCHAR2(200),
    RECEIVER_PHONE VARCHAR2(50),
    PROVINCE VARCHAR2(200),
    DISTRICT VARCHAR2(200),
    WARD VARCHAR2(200),
    STREET_DETAIL VARCHAR2(500),
    IS_DEFAULT NUMBER(1)
);

CREATE TABLE APP_ORDERS (
    ID NUMBER PRIMARY KEY,
    USER_ID NUMBER,
    STATUS VARCHAR2(50),
    TOTAL_AMOUNT NUMBER,
    RECEIVER_NAME VARCHAR2(200),
    RECEIVER_PHONE VARCHAR2(50),
    SHIPPING_ADDRESS VARCHAR2(1000),
    NOTE VARCHAR2(1000),
    PAYMENT_METHOD VARCHAR2(100),
    PAYMENT_STATUS VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE APP_ORDER_ITEMS (
    ID NUMBER PRIMARY KEY,
    ORDER_ID NUMBER,
    PRODUCT_ID NUMBER,
    PRODUCT_NAME VARCHAR2(200),
    PRODUCT_SKU VARCHAR2(100),
    UNIT_PRICE NUMBER,
    QUANTITY NUMBER,
    SUBTOTAL NUMBER,
    CONSTRAINT FK_ORDER_ITEMS_ORDER FOREIGN KEY (ORDER_ID) REFERENCES APP_ORDERS(ID)
);
