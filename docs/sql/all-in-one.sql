-- ==========================================================================
-- 1. DROP EXISTING OBJECTS (to allow fresh reinstall)
-- ==========================================================================
BEGIN
  FOR r IN (SELECT table_name FROM user_tables WHERE table_name IN ('APP_ORDER_ITEMS', 'APP_ORDERS', 'APP_USER_ADDRESSES', 'APP_PRODUCTS', 'APP_CATEGORIES', 'APP_USERS')) LOOP
    EXECUTE IMMEDIATE 'DROP TABLE "' || r.table_name || '" CASCADE CONSTRAINTS';
  END LOOP;
  
  FOR r IN (SELECT sequence_name FROM user_sequences WHERE sequence_name IN ('APP_ORDER_ITEM_SEQ', 'APP_ORDER_SEQ', 'APP_PRODUCT_SEQ', 'APP_CATEGORY_SEQ', 'APP_USER_SEQ')) LOOP
    EXECUTE IMMEDIATE 'DROP SEQUENCE "' || r.sequence_name || '"';
  END LOOP;
END;
/

