   CREATE SEQUENCE  "APP_PRODUCT_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 70 NOCACHE  NOORDER  NOCYCLE	NOKEEP	NOSCALE  GLOBAL ;


   CREATE SEQUENCE  "APP_USER_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 121 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  GLOBAL ;


  CREATE TABLE "APP_ORDERS"
   (	"ID" NUMBER(19,0),
	"USER_ID" NUMBER(19,0) NOT NULL ENABLE,
	"STATUS" VARCHAR2(30) DEFAULT 'PENDING' NOT NULL ENABLE,
	"TOTAL_AMOUNT" NUMBER(19,2) NOT NULL ENABLE,
	"RECEIVER_NAME" VARCHAR2(150) NOT NULL ENABLE,
	"RECEIVER_PHONE" VARCHAR2(20) NOT NULL ENABLE,
	"SHIPPING_ADDRESS" VARCHAR2(500) NOT NULL ENABLE,
	"NOTE" VARCHAR2(500),
	"CREATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"UPDATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"PAYMENT_METHOD" VARCHAR2(20) DEFAULT 'CASH' NOT NULL ENABLE,
	"PAYMENT_STATUS" VARCHAR2(20) DEFAULT 'PENDING' NOT NULL ENABLE,
	 PRIMARY KEY ("ID")
  USING INDEX  ENABLE
   ) ;


  CREATE TABLE "APP_ORDER_ITEMS"
   (	"ID" NUMBER(19,0),
	"ORDER_ID" NUMBER(19,0) NOT NULL ENABLE,
	"PRODUCT_ID" NUMBER(19,0) NOT NULL ENABLE,
	"PRODUCT_NAME" VARCHAR2(150) NOT NULL ENABLE,
	"PRODUCT_SKU" VARCHAR2(50) NOT NULL ENABLE,
	"UNIT_PRICE" NUMBER(19,2) NOT NULL ENABLE,
	"QUANTITY" NUMBER(10,0) NOT NULL ENABLE,
	"SUBTOTAL" NUMBER(19,2) NOT NULL ENABLE,
	 PRIMARY KEY ("ID")
  USING INDEX  ENABLE,
	 CONSTRAINT "FK_ORDER_ITEM_ORDER" FOREIGN KEY ("ORDER_ID")
	  REFERENCES "APP_ORDERS" ("ID") ON DELETE CASCADE ENABLE
   ) ;


  CREATE TABLE "APP_USERS"
   (	"ID" NUMBER(19,0) NOT NULL ENABLE,
	"USERNAME" VARCHAR2(50 CHAR) NOT NULL ENABLE,
	"FULL_NAME" VARCHAR2(100 CHAR) NOT NULL ENABLE,
	"EMAIL" VARCHAR2(150 CHAR) NOT NULL ENABLE,
	"PHONE" VARCHAR2(20 CHAR),
	"STATUS" VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL ENABLE,
	"CREATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"UPDATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"PASSWORD" VARCHAR2(100) DEFAULT '$2a$10$8.UnVuG9HHgffUDAlk8GP.3nS7G5.hGvK0c7Y5Q/AEX24y8S1mN.e',
	"ROLE" VARCHAR2(20) DEFAULT 'USER',
	 CONSTRAINT "CK_APP_USERS_STATUS" CHECK (STATUS IN ('ACTIVE', 'INACTIVE')) ENABLE,
	 CONSTRAINT "PK_APP_USERS" PRIMARY KEY ("ID")
  USING INDEX  ENABLE,
	 CONSTRAINT "UK_APP_USERS_USERNAME" UNIQUE ("USERNAME")
  USING INDEX  ENABLE,
	 CONSTRAINT "UK_APP_USERS_EMAIL" UNIQUE ("EMAIL")
  USING INDEX  ENABLE
   ) ;


  CREATE TABLE "APP_USER_ADDRESSES"
   (	"ID" NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE,
	"USER_ID" NUMBER NOT NULL ENABLE,
	"RECEIVER_NAME" VARCHAR2(100) NOT NULL ENABLE,
	"RECEIVER_PHONE" VARCHAR2(20) NOT NULL ENABLE,
	"PROVINCE" VARCHAR2(100) NOT NULL ENABLE,
	"DISTRICT" VARCHAR2(100) NOT NULL ENABLE,
	"WARD" VARCHAR2(100) NOT NULL ENABLE,
	"STREET_DETAIL" VARCHAR2(250) NOT NULL ENABLE,
	"IS_DEFAULT" NUMBER(1,0) DEFAULT 0 NOT NULL ENABLE,
	 PRIMARY KEY ("ID")
  USING INDEX  ENABLE,
	 CONSTRAINT "FK_USER_ADDRESS" FOREIGN KEY ("USER_ID")
	  REFERENCES "APP_USERS" ("ID") ON DELETE CASCADE ENABLE
   ) ;


  CREATE TABLE "APP_CATEGORIES"
   (	"ID" NUMBER(19,0),
	"NAME" VARCHAR2(100) NOT NULL ENABLE,
	"CODE" VARCHAR2(50) NOT NULL ENABLE,
	"DESCRIPTION" VARCHAR2(500),
	"CREATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"UPDATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	 PRIMARY KEY ("ID")
  USING INDEX  ENABLE,
	 UNIQUE ("CODE")
  USING INDEX  ENABLE
   ) ;


  CREATE TABLE "APP_PRODUCTS"
   (	"ID" NUMBER(19,0),
	"CATEGORY_ID" NUMBER(19,0) NOT NULL ENABLE,
	"SKU" VARCHAR2(50) NOT NULL ENABLE,
	"NAME" VARCHAR2(150) NOT NULL ENABLE,
	"DESCRIPTION" VARCHAR2(1000),
	"PRICE" NUMBER(19,2) NOT NULL ENABLE,
	"STATUS" VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL ENABLE,
	"CREATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"UPDATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
	"IMAGE_URL" VARCHAR2(500),
	"STOCK" NUMBER(10,0) DEFAULT 100 NOT NULL ENABLE,
	 PRIMARY KEY ("ID")
  USING INDEX  ENABLE,
	 UNIQUE ("SKU")
  USING INDEX  ENABLE,
	 CONSTRAINT "FK_PRODUCT_CATEGORY" FOREIGN KEY ("CATEGORY_ID")
	  REFERENCES "APP_CATEGORIES" ("ID") ON DELETE CASCADE ENABLE
   ) ;

